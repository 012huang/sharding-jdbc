package com.alibaba.druid.util;

public class StringUtils {
    
    public static int lowerHashCode(final String text) {
        if (null == text) {
            return 0;
        }
        int h = 0;
        for (int i = 0; i < text.length(); ++i) {
            char ch = text.charAt(i);
            if (ch >= 'A' && ch <= 'Z') {
                ch = (char) (ch + 32);
            }
            h = 31 * h + ch;
        }
        return h;
    }
}
